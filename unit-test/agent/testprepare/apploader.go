// loadApp performs a full Yao Runtime load from the test application directory.
// It replicates the production engine.Load() sequence (minus host/HTTP server,
// commercial license, monitor, plugin, widgets, schedule, task, robotapi) and
// then runs setup.Initialize + scripts.setup.SetupTestUsers to seed the DB
// with users/roles/teams matching what a real Yao server would have.
//
// The test process must load the complete application in-process because Yao
// is a runtime — Tai child processes tunnel back to this in-process gRPC
// server.
//
// Steps (in order, matching engine.Load):
//  1. Load config via config.Init() which reads app/.env (generated by loadAgentTestEnv)
//  2. Application + share.App
//  3. utils.Init
//  4. share.DBConnect
//  5. connector.Load
//  6. fs.Load
//  7. i18n.Load (ignored on error)
//  8. runtime.Start (V8)
//  9. query.Load
//
// 10. script.Load
// 11. model.Load (uses BatchMigrate, idempotent)
// 12. store.Load
// 13. event.Start
// 14. attachment.Load (ignored on error)
// 15. mcp.Load (ignored on error)
// 16. widgetsapp.Load (fills app.Setting for setup.Initialize)
// 17. kb.Load
// 18. agent.Load
// 19. caller/llm SetJSAPIFactory
// 20. llmprovider.Init
// 21. setting.Init
// 22. agent.SyncLLMDefaults (warn on error)
// 23. openapi.Load (initializes oauth.OAuth)
// 24. setup.Initialize (HasTable-guarded migrate + scripts.setup.Init)
// 25. scripts.setup.SetupTestUsers (idempotent)
// 26. appStartGRPC (random port)
func loadApp(t *testing.T) {
	t.Helper()

	root := agentAppDir

	os.RemoveAll(filepath.Join(root, "data", "stores"))
	cleanSQLite()

	// 1. Load config via config.Init() — reads app/.env (generated by
	// loadAgentTestEnv) through godotenv.Overload, making app/.env the
	// authoritative configuration source.
	os.Setenv("YAO_ROOT", root)
	config.Init()
	if config.Conf.DataRoot == "" {
		config.Conf.DataRoot = filepath.Join(root, "data")
	}
	os.MkdirAll(config.Conf.DataRoot, 0755)
	cfg := config.Conf

	// 2. Open application from disk and parse app.yao -> share.App
	app, err := application.OpenFromDisk(root)
	if err != nil {
		t.Fatalf("apploader: OpenFromDisk(%s): %v", root, err)
	}
	application.Load(app)
	parseAppYao(t)

	// 3. Register utility processes
	utils.Init()

	// 4. Connect database (production-quality connection pool)
	if err := share.DBConnect(cfg.DB); err != nil {
		t.Fatalf("apploader: share.DBConnect: %v", err)
	}

	// 5. Load connectors
	if err := yaoconnector.Load(cfg); err != nil {
		t.Logf("apploader: connector.Load: %v", err)
	}

	// 6. Load filesystem (critical for V8 + tai workspaces)
	if err := fs.Load(cfg); err != nil {
		t.Fatalf("apploader: fs.Load: %v", err)
	}

	// 7. Load i18n (non-fatal: missing locales dir is OK)
	if err := i18n.Load(cfg); err != nil {
		t.Logf("apploader: i18n.Load: %v", err)
	}

	// 8. Start V8 runtime (critical)
	if err := runtime.Start(cfg); err != nil {
		t.Fatalf("apploader: runtime.Start: %v", err)
	}

	// 9. Load Query Engine (non-fatal)
	if err := query.Load(cfg); err != nil {
		t.Logf("apploader: query.Load: %v", err)
	}

	// 10. Load Scripts (non-fatal: missing services/ dir is OK)
	if err := script.Load(cfg); err != nil {
		t.Logf("apploader: script.Load: %v", err)
	}

	// 11. Load Models (critical: uses BatchMigrate, skips existing tables)
	if err := yaomodel.Load(cfg); err != nil {
		t.Fatalf("apploader: model.Load: %v", err)
	}

	// 12. Load Stores (critical: __yao.store backs llmprovider/setting/oauth)
	if err := yaostore.Load(cfg); err != nil {
		t.Fatalf("apploader: store.Load: %v", err)
	}

	// 13. Start Event Service (non-fatal)
	if err := event.Start(); err != nil && err != event.ErrAlreadyStart {
		t.Logf("apploader: event.Start: %v", err)
	}

	// 14. Load Attachment Uploaders (non-fatal)
	if err := attachment.Load(cfg); err != nil {
		t.Logf("apploader: attachment.Load: %v", err)
	}

	// 15. Load MCP Clients (non-fatal)
	if err := yaomcp.Load(cfg); err != nil {
		t.Logf("apploader: mcp.Load: %v", err)
	}

	// 16. Load widgets/app DSL into widgetsapp.Setting (needed by setup.Initialize -> makeSetup)
	if err := widgetsapp.Load(cfg); err != nil {
		t.Logf("apploader: widgets/app.Load: %v", err)
	}

	// 17. Load Knowledge Base (non-fatal: assistants/* may not declare KB)
	if _, err := kb.Load(cfg); err != nil {
		t.Logf("apploader: kb.Load: %v", err)
	}

	// 18. Load Agent stack (critical: required for caller/llm factories)
	if err := agent.Load(cfg); err != nil {
		t.Fatalf("apploader: agent.Load: %v", err)
	}

	// 19. Register JSAPI factories for assistants/llm
	caller.SetJSAPIFactory()
	llm.SetJSAPIFactory()

	// 20. Initialize LLM Provider Registry (depends on store + connectors)
	if err := llmprovider.Init(); err != nil {
		t.Logf("apploader: llmprovider.Init: %v", err)
	}

	// 21. Initialize Setting Registry (depends on store)
	if err := setting.Init(); err != nil {
		t.Logf("apploader: setting.Init: %v", err)
	}

	// 22. Sync agent.yml LLM defaults (depends on agent + llmprovider + setting)
	if err := agent.SyncLLMDefaults(); err != nil {
		t.Logf("apploader: agent.SyncLLMDefaults: %v", err)
	}

	// 23. Load OpenAPI (critical: initializes oauth.OAuth for tunnel JWT)
	if _, err := openapi.Load(cfg); err != nil {
		t.Fatalf("apploader: openapi.Load: %v", err)
	}

	// 24. setup.Initialize — HasTable-guarded migrate + scripts.setup.Init (idempotent)
	if err := setup.Initialize(cfg.Root, cfg); err != nil {
		t.Logf("apploader: setup.Initialize: %v", err)
	}

	// 25. Run scripts.setup.SetupTestUsers (idempotent — script checks if users already exist)
	if err := runSetupTestUsers(t); err != nil {
		t.Logf("apploader: scripts.setup.SetupTestUsers: %v", err)
	}

	// 25b. Query database for test Team/User IDs created by SetupTestUsers
	if id, err := queryTestIdentity(); err != nil {
		t.Logf("apploader: queryTestIdentity: %v", err)
	} else {
		cachedIdentity = id
		t.Logf("apploader: Alpha team=%s (owner=%s admin=%s member=%s)",
			id.AlphaTeamID, id.AlphaOwnerUserID, id.AlphaAdminUserID, id.AlphaMemberUserID)
		t.Logf("apploader: BetaOpenAI team=%s (owner=%s admin=%s member=%s)",
			id.BetaOpenAITeamID, id.BetaOpenAIOwnerUserID, id.BetaOpenAIAdminUserID, id.BetaOpenAIMemberUserID)
		t.Logf("apploader: BetaAnthropic team=%s (owner=%s admin=%s member=%s)",
			id.BetaAnthropicTeamID, id.BetaAnthropicOwnerUserID, id.BetaAnthropicAdminUserID, id.BetaAnthropicMemberUserID)
		t.Logf("apploader: BetaHaiku team=%s (owner=%s admin=%s member=%s)",
			id.BetaHaikuTeamID, id.BetaHaikuOwnerUserID, id.BetaHaikuAdminUserID, id.BetaHaikuMemberUserID)
		t.Logf("apploader: BetaGPT4o team=%s (owner=%s admin=%s member=%s)",
			id.BetaGPT4oTeamID, id.BetaGPT4oOwnerUserID, id.BetaGPT4oAdminUserID, id.BetaGPT4oMemberUserID)
	}

	// 26. Start embedded gRPC server with TunnelHandler (random port)
	appStartGRPC(t)

	RegisterCleanup(cleanupApp)
}

// runSetupTestUsers invokes the JS Process scripts.setup.SetupTestUsers.
// The script is idempotent — it returns immediately if test users already exist.
func runSetupTestUsers(t *testing.T) error {
	t.Helper()
	p, err := process.Of("scripts.setup.SetupTestUsers")
	if err != nil {
		return fmt.Errorf("process.Of: %w", err)
	}
	res, err := p.Exec()
	if err != nil {
		return fmt.Errorf("Exec: %w", err)
	}
	t.Logf("apploader: SetupTestUsers returned: %v", res)
	return nil
}

