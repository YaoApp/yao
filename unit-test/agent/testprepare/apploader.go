type teamUsersResult struct {
	teamID    string
	ownerUID  string
	adminUID  string
	memberUID string
}

// queryTeamUsers queries the 3 users (owner/admin/member) for a team identified
// by email prefix. The owner email is used to discover the teamID via the member table,
// then admin and member userIDs are looked up by their emails.
func queryTeamUsers(prefix string) (*teamUsersResult, error) {
	ownerUID, teamID, err := queryUserTeam(prefix + "-owner@test.local")
	if err != nil {
		return nil, fmt.Errorf("owner: %w", err)
	}
	adminUID, err := queryUserID(prefix + "-admin@test.local")
	if err != nil {
		return nil, fmt.Errorf("admin: %w", err)
	}
	memberUID, err := queryUserID(prefix + "-member@test.local")
	if err != nil {
		return nil, fmt.Errorf("member: %w", err)
	}
	return &teamUsersResult{
		teamID:    teamID,
		ownerUID:  ownerUID,
		adminUID:  adminUID,
		memberUID: memberUID,
	}, nil
}

// queryUserID finds a user by email and returns their user_id.
func queryUserID(email string) (string, error) {
	p, err := process.Of("models.__yao.user.Get", map[string]interface{}{
		"wheres": []map[string]interface{}{
			{"column": "email", "value": email},
		},
		"limit": 1,
	})
	if err != nil {
		return "", fmt.Errorf("user process: %w", err)
	}
	res, err := p.Exec()
	if err != nil {
		return "", fmt.Errorf("user query: %w", err)
	}
	user, err := firstRow(res)
	if err != nil {
		return "", fmt.Errorf("user %q: %w", email, err)
	}
	return fmt.Sprintf("%v", user["user_id"]), nil
}

// queryUserTeam finds a user by email, then looks up their team via the member table.
// Returns (userID, teamID).
func queryUserTeam(email string) (string, string, error) {
	// Step 1: query user by email
	p, err := process.Of("models.__yao.user.Get", map[string]interface{}{
		"wheres": []map[string]interface{}{
			{"column": "email", "value": email},
		},
		"limit": 1,
	})
	if err != nil {
		return "", "", fmt.Errorf("user process: %w", err)
	}
	res, err := p.Exec()
	if err != nil {
		return "", "", fmt.Errorf("user query: %w", err)
	}
	user, err := firstRow(res)
	if err != nil {
		return "", "", fmt.Errorf("user %q: %w", email, err)
	}
	userID := fmt.Sprintf("%v", user["user_id"])

	// Step 2: query member table by user_id to get team_id
	p2, err := process.Of("models.__yao.member.Get", map[string]interface{}{
		"wheres": []map[string]interface{}{
			{"column": "user_id", "value": userID},
		},
		"limit": 1,
	})
	if err != nil {
		return "", "", fmt.Errorf("member process: %w", err)
	}
	res2, err := p2.Exec()
	if err != nil {
		return "", "", fmt.Errorf("member query: %w", err)
	}
	member, err := firstRow(res2)
	if err != nil {
		return "", "", fmt.Errorf("member for user %q: %w", email, err)
	}
	teamID := fmt.Sprintf("%v", member["team_id"])
	return userID, teamID, nil
}

// firstRow extracts the first map row from a model query result.
// Handles both []interface{} and typed slices like []maps.MapStrAny via reflect.
func firstRow(res interface{}) (map[string]interface{}, error) {
	if res == nil {
		return nil, fmt.Errorf("not found (nil)")
	}
	rv := reflect.ValueOf(res)
	if rv.Kind() != reflect.Slice {
		return nil, fmt.Errorf("not found (type=%T)", res)
	}
	if rv.Len() == 0 {
		return nil, fmt.Errorf("not found (empty)")
	}
	elem := rv.Index(0).Interface()
	if m, ok := elem.(map[string]interface{}); ok {
		return m, nil
	}
	// For types like maps.MapStrAny that implement a map-like interface,
	// convert via JSON round-trip.
	data, err := json.Marshal(elem)
	if err != nil {
		return nil, fmt.Errorf("not found (marshal: %v)", err)
	}
	var m map[string]interface{}
	if err := json.Unmarshal(data, &m); err != nil {
		return nil, fmt.Errorf("not found (unmarshal: %v)", err)
	}
	return m, nil
}

// cleanupApp tears down loaded resources. Called via global Cleanup() from TestMain.
func cleanupApp() {
	yaogrpc.Stop()
	event.Stop(context.Background())
	runtime.Stop()
	_ = share.DBClose()
	os.RemoveAll(filepath.Join(agentAppDir, "data", "stores"))
	cleanSQLite()
}

func parseAppYao(t *testing.T) {
	t.Helper()

	var appData []byte
	var appFile string
	var err error

	for _, name := range []string{"app.yao", "app.jsonc", "app.json"} {
		if has, _ := application.App.Exists(name); has {
			appFile = name
			appData, err = application.App.Read(name)
			if err != nil {
				t.Fatalf("apploader: read %s: %v", name, err)
			}
			break
		}
	}
	if appFile == "" {
		t.Fatal("apploader: app.yao / app.jsonc / app.json not found")
	}

	envRe := regexp.MustCompile(`\$ENV\.([0-9a-zA-Z_-]+)`)
	appData = envRe.ReplaceAllFunc(appData, func(s []byte) []byte {
		key := string(s[5:])
		if val := os.Getenv(key); val != "" {
			return []byte(val)
		}
		return s
	})

	share.App = share.AppInfo{}
	if err := application.Parse(appFile, appData, &share.App); err != nil {
		t.Fatalf("apploader: parse %s: %v", appFile, err)
	}
	if share.App.Prefix == "" {
		share.App.Prefix = "yao_"
	}
}

func cleanSQLite() {
	primary := os.Getenv("YAO_DB_PRIMARY")
	if primary == "" {
		return
	}
	if !filepath.IsAbs(primary) {
		primary = filepath.Join(agentAppDir, primary)
	}
	for _, suffix := range []string{"", "-shm", "-wal"} {
		os.Remove(primary + suffix)
	}
}

func appStartGRPC(t *testing.T) {
	t.Helper()
	if registry.Global() == nil {
		registry.Init(nil)
	}

	cfg := config.Conf
	cfg.GRPC.Enabled = "on"
	cfg.GRPC.Host = "127.0.0.1"
	cfg.GRPC.Port = 0

	if err := yaogrpc.StartServer(cfg); err != nil {
		t.Fatalf("apploader: grpc.StartServer: %v", err)
	}

	addrs := yaogrpc.Addr()
	if len(addrs) == 0 {
		t.Fatal("apploader: gRPC server started but no addresses")
	}
	_, portStr, err := net.SplitHostPort(addrs[0])
	if err != nil {
		t.Fatalf("apploader: parse gRPC addr %s: %v", addrs[0], err)
	}
	testGRPCPort, _ = strconv.Atoi(portStr)
	t.Logf("apploader: gRPC server listening on %s (port %d)", addrs[0], testGRPCPort)
}
